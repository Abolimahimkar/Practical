package Array;

public class Even_odd_Number {

	public static void main(String[] args) {
			System.out.println("Even numbers between 1 and 100:");
			for(int a=1;a<=100;a++)
			{
				if(a%2==0)
				{
					System.out.println(a+" ");
				}
			}
			System.out.println("\nodd numbers between 1 and 100:");
			for(int a=1;a<=100;a++)
			{
				if(a%2!=0)
				{
					System.out.println(a+" ");
				}
			}
				}
			}
